package org.firstinspires.ftc.teamcode.apriltag;

public class Pose2D {
    public final double x;
    public final double y;
    public final double yaw;

    public Pose2D(
            double x,
            double y,
            double yaw
    ) {
        this.x = x;
        this.y = y;
        this.yaw = yaw;
    }
}